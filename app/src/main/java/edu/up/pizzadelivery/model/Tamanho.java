package edu.up.pizzadelivery.model;

public class Tamanho {

    private int idTamanho;
    private int nome;
    private int qtdPedaços;

    public int getIdTamanho() {
        return idTamanho;
    }

    public void setIdTamanho(int idTamanho) {
        this.idTamanho = idTamanho;
    }

    public int getNome() {
        return nome;
    }

    public void setNome(int nome) {
        this.nome = nome;
    }

    public int getQtdPedaços() {
        return qtdPedaços;
    }

    public void setQtdPedaços(int qtdPedaços) {
        this.qtdPedaços = qtdPedaços;
    }
}
